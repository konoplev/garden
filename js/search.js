export function getSearchResult(...words) {
    let resultSet = null;
    words.forEach(w => {
        if (searchMap.has(w)) {
            let currentSet = searchMap.get(w);
            if (resultSet == null) {
                resultSet = currentSet;
            } else {
                //conjunction
                resultSet = new Set([...currentSet].filter(x => resultSet.has(x)));
            }
        }
    });
    if (resultSet == null) {
        return [];
    }
    return Array.from(resultSet, w => filesMap.get(w));
}
